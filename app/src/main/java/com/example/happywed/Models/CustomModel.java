package com.example.happywed.Models;

public class CustomModel {

    private int customId;
    private String customTitle;
    private String addrContact;
    private String addrNo;
    private String addrStreet;
    private String addrCity;
    private String addrDescription;
    private String customBudget;

    public int getCustomId() {
        return customId;
    }

    public CustomModel setCustomId(int customId) {
        this.customId = customId;
        return this;
    }

    public String getCustomTitle() {
        return customTitle;
    }

    public CustomModel setCustomTitle(String customTitle) {
        this.customTitle = customTitle;
        return this;
    }

    public String getAddrContact() {
        return addrContact;
    }

    public CustomModel setAddrContact(String addrContact) {
        this.addrContact = addrContact;
        return this;
    }

    public String getAddrNo() {
        return addrNo;
    }

    public CustomModel setAddrNo(String addrNo) {
        this.addrNo = addrNo;
        return this;
    }

    public String getAddrStreet() {
        return addrStreet;
    }

    public CustomModel setAddrStreet(String addrStreet) {
        this.addrStreet = addrStreet;
        return this;
    }

    public String getAddrCity() {
        return addrCity;
    }

    public CustomModel setAddrCity(String addrCity) {
        this.addrCity = addrCity;
        return this;
    }

    public String getAddrDescription() {
        return addrDescription;
    }

    public CustomModel setAddrDescription(String addrDescription) {
        this.addrDescription = addrDescription;
        return this;
    }

    public String getCustomBudget() {
        return customBudget;
    }

    public CustomModel setCustomBudget(String customBudget) {
        this.customBudget = customBudget;
        return this;
    }
}
